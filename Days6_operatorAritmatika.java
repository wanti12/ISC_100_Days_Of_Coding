package days6_operator.aritmatika;

public class Days6_operatorAritmatika {

    public static void main(String[] args) {

        int a = 5;
        int b = 2;

        int hasil1 = a + b;
        int hasil2 = a - b;
        int hasil3 = a * b;
        int hasil4 = a / b;
        int hasil5 = a % b;

        System.out.println("hasil penjumlahan = " + hasil1);
        System.out.println("hasil pengurangan = " + hasil2);
        System.out.println("hasil perkalian   = " + hasil3);
        System.out.println("hasil pembagian   = " + hasil4);
        System.out.println("hasil sisa bagi   = " + hasil5);
    }

}
